package 배열문제;

public class 타양한타입 {

	public static void main(String[] args) {
		String[] f = { "제주도", "홍콩", "오사카", "호주", "아일랜드" };
		for (String f1 : f) {
			System.out.print(f1);
		}
		char[] c = { 'r', 'g', 'b', 'p', 'w' };
		for (char c1 : c) {
			System.out.print(c1);
		}
		double[] h = { 154.6, 170.3, 177.2, 180.8 };
		for (double h1 : h) {
			System.out.print(h1);
		}

	}

}
