package 연산자;

public class 산술연산자 {

	public static void main(String[] args) {
		// 산술연산자 (+ - * %)
		
		int x = 200;
		int y = 100;
		
		System.out.println("두 수 의 합은" + (x + y));
		System.out.println("두 수 의 차는" + (x - y));
		System.out.println("두 수 의 곱은" + (x * y));
		System.out.println("두 수 의 나눗셈은" + (x / y));

	}

}
